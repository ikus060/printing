package com.patrikdufresne.printing;

import org.eclipse.swt.graphics.RGB;

/**
 * This utility class provide colors data that may be used for printing purpose.
 * 
 * @author Patrik Dufresne
 * 
 */
public class ColorUtil {

    /**
     * Contains the RGB black color;
     */
    public static final RGB BLACK = new RGB(0, 0, 0);

    /**
     * Contains the RGB white color;
     */
    public static final RGB WHITE = new RGB(255, 255, 255);

    /**
     * Contains the RGB light gray color.
     */
    public static final RGB LIGHT_GRAY = new RGB(230, 230, 230);

}
